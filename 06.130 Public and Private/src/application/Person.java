package application;

public class Person {
	// instance variables;
	private String name;
	private int age;

	/* SETTERS **/
	public void setName(String name) {
		this.name = name;
	}

	public void setAge(int age) {
		this.age = age;
	}

	/** GETTERS */
	public String getName() {
		return this.name;
	}

	public int getAge() {
		return this.age;
	}

}
