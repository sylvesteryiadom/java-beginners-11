package application;

public class Person {
	// instance variables
	String name;
	int age;

	/** SETTERS */
	void setName(String name) {
		this.name = name;
	}

	void setAge(int age) {
		this.age = age;
	}

	/** GETTERS (RETURNERS) */
	String getName() {
		return this.name;
	}

	int getAge() {
		return this.age;
	}
}
